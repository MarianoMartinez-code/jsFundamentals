// Funciones en JavaScript

// Declarar funciones
function suma(a, b) {
    return a + b;
}

// Llamar funciones
console.log(suma(1, 2));

// Funciones flecha
const resta = (a, b) => a - b;
console.log(resta(2, 1));

// Funciones anonimas o expresivas
const multiplicacion = function(a, b) {
    return a * b;
}
console.log(multiplicacion(2, 3));


